#include <bits/stdc++.h>
using namespace std;

char c;

void input() { scanf("%c", &c); }

void process() {}

void output() {
    printf("  %c\n", c);
    printf(" %c%c%c\n", c, c, c);
    printf("%c%c%c%c%c\n", c, c, c, c, c);
}

int main() {
    input();
    process();
    output();
    return 0;
}
